from __future__ import annotations

import argparse
from dataclasses import dataclass
from pathlib import Path
from typing import Iterable, Sequence

from experiments.pnt.pnt_experiments import PNTSample, generate_dataset


DEFAULT_OUTPUT = Path("experiments/pnt/output/pnt_report.md")


@dataclass(frozen=True)
class PNTSummary:
    row_count: int
    x_min: int
    x_max: int
    min_psi_error: tuple[int, float]
    max_psi_error: tuple[int, float]
    min_pi_minus_li: tuple[int, float]
    max_pi_minus_li: tuple[int, float]
    psi_error_sign_changes: int


def summarize_rows(rows: Iterable[PNTSample]) -> PNTSummary:
    ordered_rows = sorted(rows, key=lambda row: row.x)
    if not ordered_rows:
        raise ValueError("at least one PNT sample row is required")

    min_psi_error = min(ordered_rows, key=lambda row: row.psi_error)
    max_psi_error = max(ordered_rows, key=lambda row: row.psi_error)
    min_pi_minus_li = min(ordered_rows, key=lambda row: row.pi_minus_li)
    max_pi_minus_li = max(ordered_rows, key=lambda row: row.pi_minus_li)

    return PNTSummary(
        row_count=len(ordered_rows),
        x_min=ordered_rows[0].x,
        x_max=ordered_rows[-1].x,
        min_psi_error=(min_psi_error.x, min_psi_error.psi_error),
        max_psi_error=(max_psi_error.x, max_psi_error.psi_error),
        min_pi_minus_li=(min_pi_minus_li.x, min_pi_minus_li.pi_minus_li),
        max_pi_minus_li=(max_pi_minus_li.x, max_pi_minus_li.pi_minus_li),
        psi_error_sign_changes=count_psi_error_sign_changes(ordered_rows),
    )


def count_psi_error_sign_changes(rows: Iterable[PNTSample]) -> int:
    changes = 0
    previous_sign: int | None = None
    for row in rows:
        sign = value_sign(row.psi_error)
        if sign == 0:
            continue
        if previous_sign is not None and sign != previous_sign:
            changes += 1
        previous_sign = sign
    return changes


def value_sign(value: float) -> int:
    if value > 0:
        return 1
    if value < 0:
        return -1
    return 0


def render_report(rows: Iterable[PNTSample]) -> str:
    ordered_rows = sorted(rows, key=lambda row: row.x)
    summary = summarize_rows(ordered_rows)

    return "\n".join(
        [
            "# Prime Number Theorem Numerical Experiment Report",
            "",
            "This report summarizes empirical numerical data generated by "
            "`experiments.pnt.pnt_experiments`; it is not a proof of the "
            "Prime Number Theorem.",
            "",
            "## Summary",
            "",
            f"- Row count: {summary.row_count}",
            f"- x range: {summary.x_min} to {summary.x_max}",
            format_extremum("Minimum psi_error", summary.min_psi_error),
            format_extremum("Maximum psi_error", summary.max_psi_error),
            format_extremum("Minimum pi_minus_li", summary.min_pi_minus_li),
            format_extremum("Maximum pi_minus_li", summary.max_pi_minus_li),
            f"- psi_error sign changes: {summary.psi_error_sign_changes}",
            "",
            "## Notes",
            "",
            "- `psi_error = psi_x - x`.",
            "- `pi_minus_li = pi_x - li_x`.",
            "- Sign changes are counted over rows ordered by `x`, ignoring exact zero values.",
            "",
        ]
    )


def format_extremum(label: str, extremum: tuple[int, float]) -> str:
    x, value = extremum
    return f"- {label}: {value:.12g} at x={x}"


def write_report(rows: Iterable[PNTSample], output: Path = DEFAULT_OUTPUT) -> Path:
    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text(render_report(rows))
    return output


def main(argv: Sequence[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Generate a PNT Markdown experiment report.")
    parser.add_argument("--start", type=int, default=10)
    parser.add_argument("--stop", type=int, default=100_000)
    parser.add_argument("--points", type=int, default=40)
    parser.add_argument("--output", type=Path, default=DEFAULT_OUTPUT)
    args = parser.parse_args(argv)

    rows = generate_dataset(args.start, args.stop, args.points)
    output = write_report(rows, args.output)
    print(f"wrote {len(rows)} rows to {output}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
